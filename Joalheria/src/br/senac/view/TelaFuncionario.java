    private Produto tempProduto = null;
    private void jcCategoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcCategoriaItemStateChanged
        try {

            int id = jcCategoria.getSelectedIndex();

            List<Produto> produtos = ServicoDaoProduto.getIstance().filtroProduto(id);

            if (produtos == null) {
                gridProdutos.removeAll();
                return;
            }

            gridProdutos.removeAll();
            for (Produto produto : produtos) {
                JButton button = new JButton();
                button.setText(produto.getNome());

                ActionListener listener = new ActionListener() {
                    public void actionPerformed(ActionEvent ae) {
                        infoProduto(produto.getIdProduto());
                        tempProduto = produto;
                    }
                };
                button.addActionListener(listener);
                gridProdutos.add(button);
            }
            gridProdutos.revalidate();
            gridProdutos.repaint();

        } catch (DaoException ex) {
            JOptionPane.showMessageDialog(rootPane, "A pesquisa não retornou resultados ", "Sem resultados", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jcCategoriaItemStateChanged

    int size = 0;
    float valTotal = 0;
    List<Produto> listaVenda = null;
    private void btnAddCarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCarrinhoActionPerformed
        int quant = (int) jsQuant.getValue();
        int estoque = Integer.parseInt(txtQuantidade.getText());

        if (tempProduto != null) {
            if (listaVenda == null) {
                listaVenda = new ArrayList<>();
            }
            if (quant == 0) {
                JOptionPane.showMessageDialog(rootPane, "Informe uma quantidade", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (quant > tempProduto.getQuant() || quant > estoque) {
                JOptionPane.showMessageDialog(rootPane, "Quantidade superior ao estoque", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            DefaultTableModel model = (DefaultTableModel) jtListaCompra.getModel();

            for (int i = 0; i < model.getRowCount(); i++) {
                if ((int) model.getValueAt(i, 1) == tempProduto.getCodigo()) {
                    JOptionPane.showMessageDialog(rootPane, "Produto já inserido!", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            model.addRow(new Object[]{
                tempProduto.getIdProduto(),
                tempProduto.getCodigo(),
                tempProduto.getNome(),
                quant,
                tempProduto.getPrecoUni(),});

            valTotal += tempProduto.getPrecoUni() * quant;
            size++;
            updateValueTotal(size, valTotal);

            listaVenda.add(tempProduto);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um produto ", "Sem resultados", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddCarrinhoActionPerformed

    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed

        try {
            DefaultTableModel model = (DefaultTableModel) jtListaCompra.getModel();
            int index = jtListaCompra.getSelectedRow();
            float val = Float.parseFloat(jtListaCompra.getValueAt(index, 4).toString());

            model.removeRow(index);
            listaVenda.remove(index);
            valTotal -= val;
            size = model.getRowCount();
            updateValueTotal(model.getRowCount(), valTotal);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Selecione algum produto");
        }

    }//GEN-LAST:event_btnRemoveItemActionPerformed

    private void btnCancelarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarCompraActionPerformed
        if (listaVenda == null || listaVenda.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nenhuma venda iniciada", "Sem venda", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int resp = JOptionPane.showConfirmDialog(this, "Deseja cancelar Compra ?", "Cancelar", JOptionPane.YES_NO_OPTION);

        if (resp == JOptionPane.YES_OPTION) {
            clear();
        }

    }//GEN-LAST:event_btnCancelarCompraActionPerformed

    public void clear() {
        DefaultTableModel model = (DefaultTableModel) jtListaCompra.getModel();

        model.setNumRows(0);
        listaVenda.clear();
        tempProduto = null;
        size = 0;
        valTotal = 0;

        byte[] imgAux = ManipularImagem.getImgBytes(imagem);
        ManipularImagem.exibiImagemLabel(imgAux, lblImg);

        resetTnfoProduto();
        updateValueTotal(0, 0);
    }

    private void updateValueTotal(int item, float v) {
        NumberFormat format = new DecimalFormat("#0.00");
        String preco = format.format(v);

        txtTotalItens.setText(item + "");
        txtTotalCompra.setText(preco);
    }

    private void infoProduto(int id) {
        try {
            Produto p = ServicoDaoProduto.getIstance().pegaProdutoById(id);

            TipoSegmento segmento = TipoSegmento.getEnum(p.getSegmento());
            NumberFormat format = new DecimalFormat("#0.00");
            String preco = format.format(p.getPrecoUni());

            txtNome.setText(p.getNome());
            txtMarca.setText(p.getMarca());
            txtSegmento.setText(segmento.toString());
            txtMateria.setText(p.getMaterial());
            txtTamanho.setText(p.getTamanho());
            txtCodigo.setText(p.getCodigo() + "");
            txtQuantidade.setText(p.getQuant() + "");
            txtPreço.setText(preco);
            jsQuant.setValue(0);
            ManipularImagem.exibiImagemLabel(p.getImg(), lblImg);
        } catch (Exception e) {
        }
    }

    private void resetTnfoProduto() {
        txtNome.setText(null);
        txtMarca.setText(null);
        txtSegmento.setText(null);
        txtMateria.setText(null);
        txtTamanho.setText(null);
        txtCodigo.setText(null);
        txtQuantidade.setText(null);
        txtPreço.setText(null);
        jsQuant.setValue(0);
    }

