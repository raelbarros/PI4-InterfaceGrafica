    private void btnReceberCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceberCompraActionPerformed
        DefaultTableModel model = (DefaultTableModel) jtListaCompra.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum Item no Carrinho", "Sem Produtos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (telaVenda != null) {
            telaVenda.dispose();
        }

        Venda venda = new Venda();
        List<ItemVenda> listaVenda = null;

        for (int i = 0; i < model.getRowCount(); i++) {
            if (listaVenda == null) {
                listaVenda = new ArrayList<>();
            }
            ItemVenda item = new ItemVenda();
            Produto p = new Produto();

            p.setIdProduto((int) model.getValueAt(i, 0));
            p.setCodigo((int) model.getValueAt(i, 1));
            p.setNome(model.getValueAt(i, 2).toString());
            p.setPrecoUni((float) model.getValueAt(i, 4));

            item.setQuant((int) model.getValueAt(i, 3));
            item.setPrecoUni(p.getPrecoUni());
            item.setProduto(p);

            listaVenda.add(item);
        }
        venda.setListaItensVendidos(listaVenda);
        venda.setFuncionario(userLogged);

        telaVenda = new FinalizaVenda();
        telaVenda.setVisible(true);
        telaVenda.toFront();

        telaVenda.setVenda(venda);
        telaVenda.setTelaUser(this);
    }//GEN-LAST:event_btnReceberCompraActionPerformed

