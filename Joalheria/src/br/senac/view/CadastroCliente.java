    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Cliente newCliente = new Cliente();

            newCliente.setNome(txtNome.getText());
            newCliente.setSobrenome(txtSobrenome.getText());
            newCliente.setRg(txtRG.getValue().toString());
            newCliente.setCpf(txtCPF.getValue().toString());
            newCliente.setTelefone(txtTelefone.getValue().toString());
            newCliente.setEndereco(txtEndereco.getText());

            TipoSexo sexo = (TipoSexo) jcGenero.getSelectedItem();
            newCliente.setSexo(sexo.getSexoCliente());

            if (validarEmail(txtEmail.getText())) {
                newCliente.setEmail(txtEmail.getText());
            }

            Date data = null;
            DateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            data = (Date) format.parse(txtData.getValue().toString());
            newCliente.setDtNascimento(data);

            ServicoDaoCliente.getIstance().cadastraCliente(newCliente);
            JOptionPane.showMessageDialog(this, "Cadastro efetuado com sucesso!");

            txtNome.setText("");
            txtSobrenome.setText("");
            txtRG.setValue("");
            txtCPF.setValue("");
            txtTelefone.setValue("");
            txtEndereco.setText("");
            txtData.setValue("");
            txtEmail.setText("");

            telaAdmin.listarCliente();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dados invalidos!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

