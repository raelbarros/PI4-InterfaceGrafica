    private void btnCalculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculaActionPerformed
        if (!("".equals(op))) {
            switch (op) {
                case "+":
                    finalNum01 = convertStringToNumber(num01);
                    finalNum02 = convertStringToNumber(num02);
                    result = finalNum01 + finalNum02;
                    txtResult.setText("" + result);
                    break;
                case "-":
                    finalNum01 = convertStringToNumber(num01);
                    finalNum02 = convertStringToNumber(num02);
                    result = finalNum01 - finalNum02;
                    txtResult.setText("" + result);
                    break;
                case "*":
                    finalNum01 = convertStringToNumber(num01);
                    finalNum02 = convertStringToNumber(num02);
                    result = finalNum01 * finalNum02;
                    txtResult.setText("" + result);
                    break;
                case "/":
                    finalNum01 = convertStringToNumber(num01);
                    finalNum02 = convertStringToNumber(num02);
                    result = finalNum01 / finalNum02;
                    txtResult.setText("" + result);
                    break;
                default:
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione alguma operacao");
        }
    }//GEN-LAST:event_btnCalculaActionPerformed

    String op = "";
    String num01 = "";
    String num02 = "";
    float finalNum01 = 0;
    float finalNum02 = 0;
    float result = 0;

    public void apertar(String dig) {
        if ("".equals(op)) {
            num01 += dig;
            txtResult.setText(num01);

        } else {
            num02 += dig;
            txtResult.setText(num02);
        }
    }

    public void operacao(String opr) {
        op = opr;
        txtResult.setText(op);
    }

    private float convertStringToNumber(String val) {
        return Float.parseFloat(val);
    }

